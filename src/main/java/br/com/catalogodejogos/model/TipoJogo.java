package br.com.catalogodejogos.model;

public class TipoJogo {
    private int idTipoJogo;
    private String descricaoTipoJogo;

    public int getIdTipoJogo() {
        return idTipoJogo;
    }

    public void setIdTipoJogo(int idTipoJogo) {
        this.idTipoJogo = idTipoJogo;
    }

    public String getDescricaoTipoJogo() {
        return descricaoTipoJogo;
    }

    public void setDescricaoTipoJogo(String descricaoTipoJogo) {
        this.descricaoTipoJogo = descricaoTipoJogo;
    }
}
